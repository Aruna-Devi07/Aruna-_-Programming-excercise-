// Calculates area of rectangle
#include <stdio.h>

int main() {
    int width;
    int length;
    int area;

    printf("Enter Width: ");
    scanf("%d", &width);

    printf("Enter Length: ");
    scanf("%d", &length);

    area = width * length;

    printf("The Area Is %d cm^2\n", area);

    return 0;
}
